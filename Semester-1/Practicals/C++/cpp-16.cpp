#include <iostream>
using namespace std;

int main(){

    int x=10, y=20;
    //comparison operators
    cout<<"(x!=y): "<<(x!=y)<<endl;
    cout<<"(x>y): "<<(x>y)<<endl;
    cout<<"(x<y): "<<(x<y)<<endl;
    cout<<"(x<=y): "<<(x<=y)<<endl;
    cout<<"(x>=y): "<<(x>=y)<<endl;

    return 0;
}
