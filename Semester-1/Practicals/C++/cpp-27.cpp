#include <iostream>
using namespace std;

int main(){

    for(int i=0;i<10;i++)
    {
        cout<<"Hello EveryOne!!!"<<endl;
    }

    for(int i=1;i<=10;i++)
    {
        cout<<"Hello EveryOne!!!"<<endl;
    }

    for(int i=1;i<10;i++)
    {
        cout<<"Hello EveryOne!!!"<<endl;
    }

    for(int i=5;i<15;i++)
    {
        cout<<"Hello EveryOne!!!"<<endl;
    }

    return 0;

}
